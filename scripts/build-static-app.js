#!/usr/bin/env node

const fs = require("node:fs");
const path = require("node:path");

const rootDir = path.resolve(__dirname, "..");
const staticDir = path.join(rootDir, "static");
const sourceFiles = [
  "app/state.js",
  "app/utils.js",
  "app/router.js",
  "app/api.js",
  "app/credentials.js",
  "app/data.js",
  "app/notice.js",
  "app/copy.js",
  "app/shell.js",
  "app/overview.js",
  "app/skills.js",
  "app/soul.js",
  "app/cases.js",
  "app/profile.js",
  "app/submissions.js",
  "app/admin.js",
  "app/auth.js",
];

const contents = sourceFiles.map((relativePath) => {
  const absolutePath = path.join(staticDir, relativePath);
  return fs.readFileSync(absolutePath, "utf8").replace(/^\uFEFF/, "");
});

const output = [
  "/* Generated by scripts/build-static-app.js. Edit files in static/app/ instead. */",
  ...contents,
  "window.OJApp.boot();",
  "",
].join("\n\n");

fs.writeFileSync(path.join(staticDir, "app.js"), output, "utf8");
process.stdout.write(`Built static/app.js from ${sourceFiles.length} source files.\n`);
